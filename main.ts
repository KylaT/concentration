function startGame () {
    gameMode = -1
    mySplashScreen.release()
    showCards()
    createCursor()
    info.setScore(0)
    gameStartTime = game.runtime()
    info.startCountdown(NUM_SECONDS)
    gameMode = 0
}
controller.B.onEvent(ControllerButtonEvent.Pressed, function () {
    if (gameMode == 1) {
        startGame()
    }
})
function queueSelectedCard (slot: number) {
    selectedIndex = grid.getSpriteRow(cursor) * NUM_COLS + grid.getSpriteColumn(cursor)
    if (shoe[selectedIndex] > -1) {
        if (slot == 0 || selectedCards[0] != selectedIndex) {
            showCard(selectedIndex, shoe[selectedIndex])
            selectedCards[slot] = selectedIndex
            if (slot > 0) {
                testSelectedCards()
            }
        }
    }
}
function showSplashScreen () {
    mySplashScreen = infoScreens.createSplashScreen()
    mySplashScreen.setTitles(["Concentration"])
    mySplashScreen.addHeadlines(["Concentration is (C) 2019", "Robo Technical Group LLC"])
    mySplashScreen.addHeadlines(["Programmed in", "MakeCode Arcade"])
    mySplashScreen.addHeadlines(["by", "Alex K."])
    mySplashScreen.addInstructionsList(["Can you clear " + NUM_PAIRS * 2 + " cards", "in " + NUM_SECONDS + " seconds?", "", "Left/Right/Up/Down = Move", "A = Select"])
    mySplashScreen.movingSpriteMode = SpriteMode.BlankSpace
    for (let index = 0; index <= CARD_IMAGES.length - 1; index++) {
        mySplashScreen.addMovingSprite(CARD_IMAGES[index])
    }
    mySplashScreen.build()
}
function hideCard (index: number) {
    cardSprites[index].setImage(CARD_BACK)
}
function testSelectedCards () {
    if (shoe[selectedCards[0]] == shoe[selectedCards[1]]) {
        shoe[selectedCards[0]] = RESET_VALUE
        shoe[selectedCards[1]] = RESET_VALUE
        info.changeScoreBy(2)
        if (info.score() >= NUM_PAIRS * 2) {
            info.setScore(Math.floor(gameStartTime / 1000 + NUM_SECONDS - game.runtime() / 1000))
            game.over(true, effects.bubbles)
        } else {
            music.jumpUp.playUntilDone()
        }
    } else {
        music.jumpDown.playUntilDone()
        hideCard(selectedCards[0])
        hideCard(selectedCards[1])
    }
    selectedCards[0] = RESET_VALUE
    selectedCards[1] = RESET_VALUE
}
