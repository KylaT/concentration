function selectCard () {
    gameMode = -1
    if (selectedCards[0] == -1) {
        queueSelectedCard(0)
    } else {
        queueSelectedCard(1)
    }
    gameMode = 0
}
function buildShoe () {
    if (NUM_PAIRS > CARD_IMAGES.length) {
        game.splash("Not enough card images for requested deck size. Changing deck size.")
        NUM_PAIRS = CARD_IMAGES.length
    }
    if (NUM_ROWS * NUM_COLS < NUM_PAIRS * 2) {
        game.splash("Grid too small for deck size. Cannot start game.")
        game.over(false)
    }
    cardFaces = []
    for (let index = 0; index <= CARD_IMAGES.length - 1; index++) {
        cardFaces.push(index)
    }
    shuffle.shuffle(cardFaces)
    shoe = []
    for (let index = 0; index <= NUM_PAIRS - 1; index++) {
        shoe.push(cardFaces[index])
        shoe.push(cardFaces[index])
    }
    shuffle.shuffle(shoe)
}
